







<?php








/**








 * Template part for displaying the featured work section on the homepage.








 *








 * @link https://developer.wordpress.org/themes/basics/template-hierarchy/








 *








 * @package Nolan_Young_Showcase_Theme_X01








 */

















?>

















<section id="featured-work" class="section">








    <div class="container">








        <h2 class="section-title">Featured Work</h2>








        <p class="section-description">Explore our latest portfolio projects.</p>

















        <?php








        // Query for the latest 6 featured works








        $args = array(








            'post_type' => 'portfolio',








            'posts_per_page' => 6,








            'orderby' => 'date',








            'order' => 'DESC'








        );

















        $featured_works_query = new WP_Query($args);

















        if ($featured_works_query->have_posts()) :








            echo '<div class="portfolio-grid">';








            while ($featured_works_query->have_posts()) : $featured_works_query->the_post();








                get_template_part('template-parts/content', 'portfolio-card');








            endwhile;








            echo '</div>';








        else :








            echo '<p>No featured works available.</p>';








        endif;

















        wp_reset_postdata();








        ?>








    </div>








</section>








