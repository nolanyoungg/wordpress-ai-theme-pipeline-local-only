







<?php








/**








 * The template for displaying comments.








 *








 * This is the default WordPress comments template. It is used to display the comments on a post or page.








 *








 * @package Nolan Young Showcase Theme X01








 */

















/*








 * If the current post is protected by a password and the visitor has not yet entered the password we will








 * return early without loading the comments.








 */








if ( post_password_required() ) {








	return;








}








?>

















<div id="comments" class="comments-area">

















	<?php








	// You can start editing here -- including this comment!








	if ( have_comments() ) :








		?>








		<h2 class="comments-title">








			<?php








			$comment_count = get_comments_number();








			if ( '1' === $comment_count ) {








				printf(








					/* translators: 1: title of the post */








					esc_html_x( 'One thought on &ldquo;%s&rdquo;', 'comments title', 'nolan-young-showcase-theme-x01' ),








					'<span>' . get_the_title() . '</span>'








				);








			} else {








				printf( // WPCS: XSS OK.








					/* translators: 1: number of comments, 2: title of the post */








					esc_html_x( '%1$s thoughts on &ldquo;%2$s&rdquo;', 'comments title', 'nolan-young-showcase-theme-x01' ),








					number_format_i18n( $comment_count ),








					'<span>' . get_the_title() . '</span>'








				);








			}








			?>








		</h2><!-- .comments-title -->

















		<?php the_comments_navigation(); ?>

















		<ol class="comment-list">








			<?php








			wp_list_comments( array(








				'style'      => 'ol',








				'short_ping' => true,








				'reply_text' => esc_html__( 'Reply', 'nolan-young-showcase-theme-x01' ),








			) );








			?>








		</ol><!-- .comment-list -->

















		<?php








		the_comments_navigation();

















		// If comments are closed and there are comments, let's leave a little note, shall we?








		if ( ! comments_open() ) :








			?>








			<p class="no-comments"><?php esc_html_e( 'Comments are closed.', 'nolan-young-showcase-theme-x01' ); ?></p>








			<?php








		endif;

















	endif; // Check for have_comments().

















	comment_form();








	?>

















</div><!-- #comments -->








