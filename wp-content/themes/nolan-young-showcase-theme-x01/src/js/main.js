document.addEventListener('DOMContentLoaded', () => {








    initHeaderMenu();








    initMobileDrawer();








    initRailPanels();








    initPortfolioFilter();








    initCarousel();








    initBeforeAfter();








    initTestimonials();








    initScrollReveal();








});

















function initHeaderMenu() {








    const menuItems = document.querySelectorAll('[data-menu-item]');








    const dropdowns = document.querySelectorAll('[data-menu-dropdown]');

















    menuItems.forEach(item => {








        item.addEventListener('click', () => {








            const targetDropdown = document.querySelector(`[data-menu-dropdown="${item.getAttribute('data-menu-item')}"]`);








            toggleMenu(targetDropdown);








        });








    });

















    document.addEventListener('keydown', (e) => {








        if (e.key === 'Escape') {








            closeAllMenus();








        }








    });

















    document.addEventListener('click', (e) => {








        if (!document.querySelector('.header-menu').contains(e.target)) {








            closeAllMenus();








        }








    });








}

















function toggleMenu(dropdown) {








    dropdowns.forEach(d => d.classList.remove('active'));








    dropdown.classList.toggle('active');








}

















function closeAllMenus() {








    dropdowns.forEach(d => d.classList.remove('active'));








}

















function initMobileDrawer() {








    const drawer = document.querySelector('.mobile-drawer');








    const openButton = document.querySelector('.mobile-drawer-open');








    const closeButton = document.querySelector('.mobile-drawer-close');

















    openButton.addEventListener('click', () => {








        drawer.classList.add('open');








        document.body.style.overflow = 'hidden';








    });

















    closeButton.addEventListener('click', () => {








        drawer.classList.remove('open');








        document.body.style.overflow = '';








    });

















    document.addEventListener('keydown', (e) => {








        if (e.key === 'Escape') {








            drawer.classList.remove('open');








            document.body.style.overflow = '';








        }








    });








}

















function initRailPanels() {








    const railItems = document.querySelectorAll('[data-rail-item]');








    const railContents = document.querySelectorAll('[data-rail-content]');

















    railItems.forEach(item => {








        item.addEventListener('click', () => {








            railContents.forEach(content => content.classList.remove('active'));








            const targetContent = document.querySelector(`[data-rail-content="${item.getAttribute('data-rail-item')}"]`);








            targetContent.classList.add('active');








        });








    });

















    railItems.forEach(item => {








        item.addEventListener('keydown', (e) => {








            if (e.key === 'Enter' || e.key === ' ') {








                const targetContent = document.querySelector(`[data-rail-content="${item.getAttribute('data-rail-item')}"]`);








                targetContent.classList.add('active');








            }








        });








    });








}

















function initPortfolioFilter() {








    const filterButtons = document.querySelectorAll('.portfolio-filter button');








    const portfolioItems = document.querySelectorAll('.portfolio-item');

















    filterButtons.forEach(button => {








        button.addEventListener('click', () => {








            filterButtons.forEach(btn => btn.classList.remove('active'));








            button.classList.add('active');

















            const filterValue = button.getAttribute('data-filter');








            portfolioItems.forEach(item => {








                if (filterValue === 'all' || item.classList.contains(filterValue)) {








                    item.style.display = 'block';








                } else {








                    item.style.display = 'none';








                }








            });








        });








    });








}

















function initCarousel() {








    const carousel = document.querySelector('.carousel');








    let currentIndex = 0;

















    function showSlide(index) {








        const slides = carousel.querySelectorAll('.slide');








        slides.forEach((slide, i) => {








            slide.style.transform = `translateX(${100 * (i - index)}%)`;








        });








    }

















    setInterval(() => {








        currentIndex = (currentIndex + 1) % carousel.children.length;








        showSlide(currentIndex);








    }, 3000);

















    showSlide(currentIndex);








}

















function initBeforeAfter() {








    const beforeAfter = document.querySelector('.before-after');








    let slider = beforeAfter.querySelector('.slider');








    let sliderValue = 50;

















    slider.addEventListener('input', () => {








        sliderValue = slider.value;








        updateBeforeAfter();








    });

















    function updateBeforeAfter() {








        beforeAfter.style.backgroundImage = `linear-gradient(to right, #fffaf3 ${sliderValue}%, #eadfce ${sliderValue}%)`;








    }

















    updateBeforeAfter();








}

















function initTestimonials() {








    const testimonials = document.querySelector('.testimonials');








    let currentIndex = 0;

















    setInterval(() => {








        currentIndex = (currentIndex + 1) % testimonials.children.length;








        showTestimonial(currentIndex);








    }, 5000);

















    function showTestimonial(index) {








        testimonials.querySelectorAll('.testimonial').forEach((testimonial, i) => {








            testimonial.style.display = i === index ? 'block' : 'none';








        });








    }

















    showTestimonial(currentIndex);








}

















function initScrollReveal() {








    const revealElements = document.querySelectorAll('.scroll-reveal');

















    function checkVisibility() {








        revealElements.forEach(element => {








            if (isElementInViewport(element)) {








                element.classList.add('revealed');








            }








        });








    }

















    function isElementInViewport(el) {








        const rect = el.getBoundingClientRect();








        return (








            rect.top >= 0 &&








            rect.left >= 0 &&








            rect.bottom <= (window.innerHeight || document.documentElement.clientHeight) &&








            rect.right <= (window.innerWidth || document.documentElement.clientWidth)








        );








    }

















    window.addEventListener('scroll', checkVisibility);








    window.addEventListener('resize', checkVisibility);








    checkVisibility();








}














