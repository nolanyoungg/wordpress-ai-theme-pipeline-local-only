







<?php








/**








 * Theme Setup and Initialization








 *








 * @package Nolan Young Showcase Theme X01








 */

















if ( ! defined( 'ABSPATH' ) ) {








    exit; // Exit if accessed directly.








}

















/**








 * Sets up theme defaults and registers support for various WordPress features.








 */








function nolan_young_setup() {








    // Add default posts and comments RSS feed links to head.








    add_theme_support( 'automatic-feed-links' );

















    // Let WordPress manage the document title.








    add_theme_support( 'title-tag' );

















    // Enable support for post thumbnails on posts and pages.








    add_theme_support( 'post-thumbnails' );

















    // Register primary navigation menu.








    register_nav_menus(








        array(








            'primary' => esc_html__( 'Primary Menu', 'nolan-young-showcase-theme-x01' ),








        )








    );

















    // Add support for HTML5 markup structure.








    add_theme_support(








        'html5',








        array(








            'search-form',








            'comment-form',








            'comment-list',








            'gallery',








            'caption',








            'style',








            'script',








        )








    );

















    // Add theme support for selective refresh for widgets.








    add_theme_support( 'customize-selective-refresh-widgets' );

















    // Add support for custom logo.








    add_theme_support(








        'custom-logo',








        array(








            'height'      => 100,








            'width'       => 400,








            'flex-height' => true,








            'flex-width'  => true,








        )








    );

















    // Add support for responsive embeds.








    add_theme_support( 'responsive-embeds' );








}

















add_action( 'after_setup_theme', 'nolan_young_setup' );

















/**








 * Enqueues scripts and styles for the theme.








 */








function nolan_young_enqueue_scripts() {








    $theme_version = wp_get_theme()->get( 'Version' );

















    // Enqueue main stylesheet.








    wp_enqueue_style(








        'nolan-young-style',








        get_template_directory_uri() . '/assets/css/main.css',








        array(),








        $theme_version








    );

















    // Enqueue main JavaScript file if it exists.








    if ( file_exists( get_template_directory() . '/assets/js/bundle.js' ) ) {








        wp_enqueue_script(








            'nolan-young-script',








            get_template_directory_uri() . '/assets/js/bundle.js',








            array(),








            $theme_version,








            true








        );








    }








}

















add_action( 'wp_enqueue_scripts', 'nolan_young_enqueue_scripts' );

















/**








 * Registers widget areas.








 */








function nolan_young_widgets_init() {








    register_sidebar(








        array(








            'name'          => esc_html__( 'Footer Widgets', 'nolan-young-showcase-theme-x01' ),








            'id'            => 'footer-1',








            'description'   => esc_html__( 'Add widgets here to appear in your footer.', 'nolan-young-showcase-theme-x01' ),








            'before_widget' => '<section id="%1$s" class="widget %2$s">',








            'after_widget'  => '</section>',








            'before_title'  => '<h2 class="widget-title">',








            'after_title'   => '</h2>',








        )








    );








}

















add_action( 'widgets_init', 'nolan_young_widgets_init' );

















