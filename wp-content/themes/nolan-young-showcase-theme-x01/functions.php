function nolan_young_showcase_theme_setup() {








    // Add support for title tag








    add_theme_support('title-tag');

















    // Add support for post thumbnails








    add_theme_support('post-thumbnails');

















    // Add support for custom logo








    add_theme_support('custom-logo', array(








        'height'      => 100,








        'width'       => 400,








        'flex-height' => true,








        'flex-width'  => true,








    ));

















    // Add support for HTML5








    add_theme_support('html5', array(








        'search-form',








        'comment-form',








        'comment-list',








        'gallery',








        'caption',








    ));

















    // Register primary navigation menu








    register_nav_menus(array(








        'primary' => __('Primary Menu', 'nolan-young-showcase-theme-x01'),








    ));








}








add_action('after_setup_theme', 'nolan_young_showcase_theme_setup');

















function nolan_young_showcase_theme_enqueue_scripts() {








    // Enqueue main CSS








    wp_enqueue_style('nolan-young-showcase-theme-main-css', get_template_directory_uri() . '/assets/css/main.css', array(), '1.0', 'all');

















    // Enqueue bundle JS if it exists








    if (file_exists(get_template_directory() . '/assets/js/bundle.js')) {








        wp_enqueue_script('nolan-young-showcase-theme-bundle-js', get_template_directory_uri() . '/assets/js/bundle.js', array(), '1.0', true);








    }








}








add_action('wp_enqueue_scripts', 'nolan_young_showcase_theme_enqueue_scripts');








