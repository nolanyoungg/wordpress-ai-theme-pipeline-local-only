 * The template for displaying the portfolio page.








 */

















get_header();








?>

















<main id="primary" class="site-main">

















	<header class="page-header">








		<h1 class="page-title"><?php the_title(); ?></h1>








	</header><!-- .page-header -->

















	<div class="portfolio-filter">








		<button class="filter-button active" data-filter="*">All</button>








		<button class="filter-button" data-filter=".portrait-sessions">Portrait Sessions</button>








		<button class="filter-button" data-filter=".weddings-engagements">Weddings & Engagements</button>








		<button class="filter-button" data-filter=".events-celebrations">Events & Celebrations</button>








		<button class="filter-button" data-filter=".brand-photography">Brand Photography</button>








		<button class="filter-button" data-filter=".product-detail-photography">Product & Detail Photography</button>








		<button class="filter-button" data-filter=".lifestyle-family-sessions">Lifestyle & Family Sessions</button>








	</div>

















	<div class="portfolio-grid">








		<?php








		$args = array(








			'post_type' => 'portfolio',








			'posts_per_page' => -1,








			'orderby' => 'date',








			'order' => 'DESC'








		);

















		$query = new WP_Query($args);

















		if ($query->have_posts()) :








			while ($query->have_posts()) : $query->the_post();








				$categories = get_the_terms(get_the_ID(), 'portfolio_category');








				$category_classes = array();

















				if (!empty($categories)) {








					foreach ($categories as $category) {








						$category_classes[] = sanitize_html_class($category->slug);








					}








				}

















				?>








				<div class="portfolio-item <?php echo esc_attr(implode(' ', $category_classes)); ?>">








					<a href="<?php the_permalink(); ?>">








						<?php the_post_thumbnail('large'); ?>








						<div class="portfolio-overlay">








							<h3><?php the_title(); ?></h3>








							<p><?php the_excerpt(); ?></p>








						</div>








					</a>








				</div>








				<?php








			endwhile;








			wp_reset_postdata();








		else :








			echo '<p>No portfolio items found.</p>';








		endif;








		?>








	</div>

















</main><!-- #primary -->

















<?php








get_footer();








?>








