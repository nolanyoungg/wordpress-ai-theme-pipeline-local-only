







<?php








/**








 * Template Name: Resources Page








 *








 * @package Nolan_Young_Showcase_Theme_X01








 */

















get_header();








?>

















<main id="primary" class="site-main">

















	<header class="page-header">








		<h1 class="page-title"><?php the_title(); ?></h1>








	</header><!-- .page-header -->

















	<div class="resources-container">








		<?php








		// Query for resources posts








		$args = array(








			'post_type' => 'resource',








			'posts_per_page' => -1,








			'orderby' => 'date',








			'order' => 'DESC'








		);

















		$resources_query = new WP_Query($args);

















		if ($resources_query->have_posts()) :








			while ($resources_query->have_posts()) : $resources_query->the_post();








				?>








				<div class="resource-card">








					<?php if (has_post_thumbnail()) : ?>








						<div class="resource-image">








							<?php the_post_thumbnail('large'); ?>








						</div>








					<?php endif; ?>

















					<div class="resource-content">








						<h2 class="resource-title"><a href="<?php the_permalink(); ?>"><?php the_title(); ?></a></h2>








						<div class="resource-excerpt"><?php the_excerpt(); ?></div>








						<a href="<?php the_permalink(); ?>" class="btn btn-primary">Read More</a>








					</div>








				</div>








				<?php








			endwhile;








			wp_reset_postdata();








		else :








			echo '<p>No resources found.</p>';








		endif;








		?>








	</div><!-- .resources-container -->

















</main><!-- #primary -->

















<?php








get_footer();








?>








